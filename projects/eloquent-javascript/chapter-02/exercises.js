function drawChessboard(x) {
var board = [];
//iterate
for(var i = 0; i < x; i++){
  var row;
  //if index is even
  if(i % 2 === 0){
    row = " ";
    for(let i = 1; i < x; i++){
      if(i % 2 === 0){
        row += " ";
      }else{
        row += "#";
      }
    }
    //if index is odd
  }else{
    row = "#";
    for (let i = 1; i < x; i++){
      if(i % 2 === 0){
        row += "#";
      }else{
        row += " ";
      }
    }
  }
  board.push(row);
}
return board.join("\n");
}
drawChessboard(8);
